
x = 10

# Augmented Assignment Operators
x += 5  
print(x)  

x -= 3  
print(x)  

x *= 2  
print(x) 

x /= 4 
print(x)  

x //= 2  
print(x)  

x %= 2  
print(x)  

x **= 3  
print(x)  


y = 4
y &= 2  
print(y)  

y |= 5  
print(y)  

y ^= 3  
print(y) 

y <<= 1  
print(y) 

y >>= 2  
print(y)  
