# Escape sequence examples
print("Hello\nWorld!")     # Newline (\n)
print("Hello\tWorld!")     # Tab (\t)
print("He said, \"Python is great!\"")  # Double quote (\")
print('It\'s a beautiful day!')   # Single quote (\')
print("Backslash: \\")      # Backslash (\\)
print("Unicode: \u2764")    # Unicode for a heart symbol (‚ù§)

